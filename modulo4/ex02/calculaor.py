#!/usr/bin/env python3 

num1 = int(input("Me de o primeiro numero: "))
num2 = int(input("Me de o segundo numero: "))

soma = num1 + num2
sub = num1 - num2
divisao = int(num1 / num2)
multi = num1 * num2

print(f"{num1} + {num2} = {soma}")
print(f"{num1} - {num2} = {sub}")
print(f"{num1} / {num2} = {divisao}")
print(f"{num1} * {num2} = {multi}")
